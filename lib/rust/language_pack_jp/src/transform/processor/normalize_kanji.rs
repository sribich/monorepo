/// Converts kanji variants to their standard dictionary form.
///
/// For example:
///
///   - 萬 -> 万
///
/// # Examples
///
/// ```rust
/// use language_pack::TextProcessor;
/// use language_pack_jp::transform::processor::NormalizeKanji;
///
/// let processor = NormalizeKanji {};
///
/// assert_eq!("万", processor.process("萬".into()).text);
/// ```
pub struct NormalizeKanji;

impl TextProcessor for NormalizeKanji {
    fn matches(&self, text: &str) -> bool {
        text.chars().any(|c| KANJI_MAPPINGS.contains_key(&c))
    }

    fn process(&self, transform: Transform) -> Transform {
        let line = transform
            .text
            .chars()
            .map(|c| KANJI_MAPPINGS.get(&c).map(|c| *c).unwrap_or(c))
            .collect();

        Transform {
            text: line,
            mappings: vec![],
            previous: Some(Box::new(transform)),
        }
    }
}

